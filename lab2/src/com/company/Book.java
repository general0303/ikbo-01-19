package com.company;

public class Book {

    private String author;
    private String tittle;
    private int year;

    public void setAuthor(String author) {
        this.author = author;
    }

    public void setTittle(String tittle) {
        this.tittle = tittle;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getAuthor() {
        return author;
    }

    public String getTittle() {
        return tittle;
    }

    public int getYear() {
        return year;
    }
}
